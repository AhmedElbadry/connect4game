void DrawGrid(){
	
	int cellW = 8;
	int cellH = 5;
	int hLineW = 1;
	int vLineW = 2;
	int numOfCol = 7;
	int numOfRow = 6;
	int leftMargin = (SCREENW - (cellW * numOfCol + vLineW*(numOfCol+1)))/2;
	int topMargin = SCREENH - (cellH*numOfRow +  vLineW*numOfRow);
	
	//draw vertical lines
	int yPos = SCREENH - 1;
	int xPos = 0;
	int i = 0;
	while( i <= numOfCol){
		xPos = i*(cellW + vLineW);
		Nokia5110_PrintBMP(xPos + leftMargin,  yPos, vvLine, 0);
		i++;
	}
	
	
	//draw vertical lines
	yPos = 0;
	xPos = leftMargin;
	i = 0;
	while( i < numOfRow){
		yPos = SCREENH - ( 1 +  i*(cellH + hLineW));
		Nokia5110_PrintBMP(xPos, yPos, hLine, 0);
		
		i++;
	}
	
}



